void solve()
{
    int n;
    cin>>n;
    int x=sqrt(n);
    int ans=INT_MAX;
    for (int i = 1; i <= x; i++)
    {
        if(n%i==0){
            ans=min(ans,i-1+n/i-1);
        }
    }
    cout<<ans<<endl;
    
}

